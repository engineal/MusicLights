var stompClient = null;

function setConnected(connected) {
    $("#connect").prop("disabled", connected);
    $("#disconnect").prop("disabled", !connected);
    if (connected) {
        $("#conversation").show();
    } else {
        $("#conversation").hide();
    }
    $("#greetings").html("");
}

function connect() {
    var socket = new SockJS('/gs-guide-websocket');
    stompClient = Stomp.over(socket);
    stompClient.connect({}, function (frame) {
        setConnected(true);
        console.log('Connected: ' + frame);
        stompClient.subscribe('/topic/greetings', function (greeting) {
            showGreeting(JSON.parse(greeting.body).content);
        });
    }, function (message) {
        if (message.includes("Whoops! Lost connection")) {
            disconnect();
        }
    });
}

function disconnect() {
    if (stompClient !== null) {
        stompClient.disconnect();
    }
    setConnected(false);
    console.log("Disconnected");
}

function sendName() {
    stompClient.send("/app/hello", {}, JSON.stringify({'name': $("#name").val()}));
}

function sendColor() {
    stompClient.send("/app/color", {}, JSON.stringify({
        'red': parseFloat($("#color-r").val()),
        'green': parseFloat($("#color-g").val()),
        'blue': parseFloat($("#color-b").val()),
        'alpha': 255
    }));
}

function sendEffect() {
    stompClient.send("/app/effect", {}, JSON.stringify({
        'type': '.ChristmasEffect'
    }));
}

function showGreeting(message) {
    $("#greetings").append("<tr><td>" + message + "</td></tr>");
}

$(function () {
    $("form").on('submit', function (e) {
        e.preventDefault();
    });
    $("#connect").click(function () {
        connect();
    });
    $("#disconnect").click(function () {
        disconnect();
    });
    $("#send").click(function () {
        sendName();
    });
    $("input[type=range]").on('change', function(){
        sendColor();
    });
    $("#christmas").click(function () {
        sendEffect();
    });
});